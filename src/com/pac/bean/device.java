package com.pac.bean;

public class device {

	/**
	 * 设备id
	 */
	private int deviceid;
	
	/**
	 * 设备当前状态
	 */
	private int status;
	public int getDeviceid() {
		return deviceid;
	}
	public void setDeviceid(int deviceid) {
		this.deviceid = deviceid;
	}
	public int getStatus() {
		return status;
	}
	public void setStatus(int status) {
		this.status = status;
	}
	
}
